// Switch case

/* 
basic syntax:

switch (key) {
  case value:
    break;
  default:
    break;
}
*/
const month = 4;

switch (month) {
  case 1:
    console.log("Jan");
    break;

  case 2:
    console.log("Feb");
    break;

  case 3:
    console.log("Mar");
    break;

  case 4:
    console.log("Apr");
    break;

  case 5:
    console.log("May");
    break;

  case 6:
    console.log("Jun");
    break;

  default:
    break;
}
